# pip



# Can install libraries and packages from pypi.org (made freely by people)

# In powershell or cmd terminal:
    # pip install (name of the library)



# Can upload the latest version of the library

# In powershell or cmd terminal:
    # pip install -U (name of the library)



# Can uninstall the library

# In powershell or cmd terminal:
    # pip uninstall (name of the library)



# Can show information about the library

# In powershell or cmd terminal:
    # pip show (name of the library)